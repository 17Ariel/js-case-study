var states = "CALIFORNIA";

if (states == "CALIFORNIA" || states == "california")
	{
		document.write("CA/ca");
	}
else if (states == "CONNECTICUT" || states == "connecticut")
	{
		document.write("CT\ct");
	}
else if (states == "HAWAII" || states == "hawaii")
	{
		document.write("HI/hi");
	}
else if (states == "New YORK" || states == "")
	{
		document.write("NY/na");
	}
else if (states == "WASHINGTON" || states == "washington")
	{
		document.write("WA/wa");
	}
else if (states == "TENNESE" || states == "tennese")
	{
		document.write("TN/tn");
	}
else
	{
		document.write("invalid abbrevation");
	}
	
